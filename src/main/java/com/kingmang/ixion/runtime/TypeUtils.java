package com.kingmang.ixion.runtime;

public class TypeUtils {

    public static Integer Int(int arg){
        return arg;
    }


    public static Float Float(float arg){
        return arg;
    }




    public static int toInt(Object arg){
        return (int) arg;
    }

    public static float toFloat(Object arg){
        return (float) arg;
    }

}
